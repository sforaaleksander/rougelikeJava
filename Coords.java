public class Coords {
    private int posY;
    private int posX;

    public Coords(int posY, int posX){
        this.posY = posY;
        this.posX = posX;
    }

    public int getPosY(){
        return posY;
    }

    public int getPosX(){
        return posX;
    }

    public void setPosY(int posY){
        this.posY = posY;
    }

    public void setPosX(int posX){
        this.posX = posX;
        }
}