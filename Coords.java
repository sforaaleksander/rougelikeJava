public class Coords {
    private int posY;
    private int posX;

    public Coords(int posY, int posX){
        this.posY = posY;
        this.posX = posX;
    }

    public int getposY(){
        return posY;
    }

    public int getposX(){
        return posX;
    }

    public void setposY(int posY){
        this.posY = posY;
    }

    public void setposX(int posX){
        this.posY = posX;
        }


}